#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-n);
	}
	else if (n > 0)
	{
		if (n / 10 != 0)
			ft_putnbr(n / 10);
		write(1, &"0123456789"[n % 10], 1);
	}
}
