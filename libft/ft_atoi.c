#include "libft.h"

int	ft_atoi(const char *str)
{
	char		*s;
	long long	neg;
	long long	ret;

	ret = 0;
	neg = 1;
	s = (char *)str;
	while (ft_isspace(*s))
		s++;
	if (*s == '-')
		neg = -1;
	if (*s == '-' || *s == '+')
		s++;
	while (ft_isdigit(*s))
	{
		if (neg == 1 && ((ret == 922337203685477580 && *s > '7')
				|| ret > 922337203685477580) && ret < 1000000000000000000)
			return (-1);
		if (neg == -1 && ((ret == 922337203685477580 && *s > '8')
				|| ret > 922337203685477580) && ret < 1000000000000000000)
			return (0);
		ret *= 10;
		ret += (*s++ - '0');
	}
	return ((int)(ret * neg));
}
